package com.xie.OperatorDemo1;

public class OperatorDemo4 {
    public static void main(String[] args) {
        // 目標: 掌握關係運算符
        int a = 10;
        int b = 5;
        boolean rs = a > b;
        System.out.println(rs); // true
        System.out.println(a >= b); //true
        System.out.println(a < b); // false
        System.out.println(a <= b); // false
        System.out.println(a == b); // false
        System.out.println(a == 10); // true
        System.out.println(a != b); // true
    }
}
