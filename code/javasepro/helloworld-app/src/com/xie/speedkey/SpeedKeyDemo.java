package com.xie.speedkey;

/**
 這是文檔註釋
 可以多行
 */
public class SpeedKeyDemo {
    public static void main(String[] args) {
    // 這是單行註釋
        System.out.println("Hello World");
        System.out.println("Hello World");
    /*
        這是多行註釋
        可以多
        多多
     */
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
    }
}
