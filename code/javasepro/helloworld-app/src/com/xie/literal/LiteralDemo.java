package com.xie.literal;

public class LiteralDemo {
    public static void main(String[] args) {
        // 目標: 掌握數據的格式
        // 1. 整數
        System.out.println(666);

        // 2. 小數
        System.out.println(99.1);

        // 3. 字符
        // \n : 換行 , \t : tab
        System.out.println('a');

        // 4. 字符串
        System.out.println("今天很哈皮");

        // 5. 布林值
        System.out.println(true);

        // 6. 空值
        System.out.println( );
    }
}
